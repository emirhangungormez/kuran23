import fs from 'node:fs'
import path from 'node:path'

const inputPath = path.resolve(process.argv[2] || './tmp/tts-lexicon-focused/lemma-groups-top200.json')
const outputPath = path.resolve(process.argv[3] || './src/data/generatedTtsLemmaLexicon.js')

const QUOTE_REGEX = /[’'`´]/gu
const SUFFIX_TAIL_REGEX = /^(?:da|de|ta|te|dan|den|tan|ten|a|e|ya|ye|i|ı|u|ü|yi|yı|yu|yü|in|ın|un|ün|nin|nın|nun|nün|na|ne|nda|nde|ndan|nden|lar|ler|lari|leri|ların|lerin)$/u

function normalizeQuotes(value) {
  return String(value || '').replace(QUOTE_REGEX, "'")
}

function normalizeCompareKey(value) {
  return normalizeQuotes(value)
    .toLocaleLowerCase('tr-TR')
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9çğıöşü]/g, '')
}

function hasSuffixDrift(root, pronunciation) {
  const rootKey = normalizeCompareKey(root)
  const pronKey = normalizeCompareKey(pronunciation)
  if (!rootKey || !pronKey) return true
  if (!pronKey.startsWith(rootKey)) return false

  const tail = pronKey.slice(rootKey.length)
  if (!tail) return false
  return SUFFIX_TAIL_REGEX.test(tail)
}

function isSafeAcceptedGroup(group) {
  if (!group || group.kuralDurumu !== 'Kabul') return false
  const root = String(group.temsilKok || '').trim()
  const pronunciation = String(group.onerilenOkunus || '').trim()
  const regexRule = String(group.regexKurali || '').trim()
  if (!root || !pronunciation || !regexRule) return false
  if (hasSuffixDrift(root, pronunciation)) return false
  return true
}

function emitGeneratedLexicon(groups, meta = {}) {
  const lines = [
    '// Generated by scripts/build-tts-focused-runtime-lexicon.mjs',
    `// Source: ${meta.source || ''}`,
    `// Safe accepted groups: ${meta.count || groups.length}`,
    'export const GENERATED_TTS_LEMMA_LEXICON = ['
  ]

  groups.forEach((group) => {
    const regexLiteral = String(group.regexKurali || '').replace(/^\/|\/[gimsuy]*$/g, '')
    const flagsMatch = String(group.regexKurali || '').match(/\/([gimsuy]+)$/)
    const flags = flagsMatch ? flagsMatch[1] : 'giu'
    const safePronunciation = String(group.onerilenOkunus || '')
      .replace(/\\/g, '\\\\')
      .replace(/'/g, "\\'")
    lines.push(`  [/${regexLiteral}/${flags}, '${safePronunciation}'],`)
  })

  lines.push(']')
  lines.push('')
  return lines.join('\n')
}

function main() {
  if (!fs.existsSync(inputPath)) {
    console.error(`Input file not found: ${inputPath}`)
    process.exit(1)
  }

  const raw = fs.readFileSync(inputPath, 'utf8')
  const parsed = JSON.parse(raw)
  const groups = Array.isArray(parsed.groups) ? parsed.groups : []
  const safeAccepted = groups
    .filter(isSafeAcceptedGroup)
    .sort((a, b) => (Number(b.toplamFrekans || 0) - Number(a.toplamFrekans || 0)))

  const content = emitGeneratedLexicon(safeAccepted, {
    source: inputPath,
    count: safeAccepted.length
  })

  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, content, 'utf8')

  console.log(`Generated: ${outputPath}`)
  console.log(`Safe rules: ${safeAccepted.length}`)
}

main()
